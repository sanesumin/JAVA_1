package day0604.operator;

public class DenyLogicOperatorExample {

	public static void main(String[] args) {
		boolean play = true;
		System.out.println(play);
		
		play=!play;
		System.out.println(play);
		
		
		play=!play;
		System.out.println(play);
		//부정의 부정은 긍정이다.
		
	}

}
